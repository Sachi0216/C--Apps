using System;
using System.Collections.Generic;
using System.Text;

namespace Methods
{
    public class Methods
    {
        public int Add(int data, int Value)
        {

            return data + Value;
        }

        public int Subtract(int data, int value)
        {
            return data - value;
        }

        public int Divide(int data, int value)
        {
            return data / value;
        }
    } // all methods returning an integer
}
